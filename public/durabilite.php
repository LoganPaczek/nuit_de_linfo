                <h1>Informatique durable : c'est quoi ? </h1>
                <p>
L’informatique durable (ou numérique responsable / green IT) cherche à réduire l’empreinte du numérique sur tout le cycle de vie : fabrication, usage et fin de vie des équipements. Cela passe par des choix comme allonger la durée de vie du matériel, optimiser la consommation d’énergie, limiter les achats neufs et développer des pratiques de réemploi et de réparation.​</p>
            </div>
