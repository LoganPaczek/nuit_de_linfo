            <div class="paragraphe2">
                <p>Le reconditionnement comme acte de durabilité
Le reconditionnement consiste à récupérer des ordinateurs usagés, effacer les données, nettoyer et réparer, remplacer éventuellement des pièces, puis réinstaller un système (souvent Linux) et des logiciels adaptés pour leur donner une seconde vie. En prolongeant un PC de 2 à 4 ans, on peut réduire d’environ 50% son empreinte carbone liée au cycle de vie, puisqu’une grande partie de l’impact vient de la fabrication initiale.​</p>
            </div>
            </div>
            <div CLASS="paragraphe3">
                <p>Un exemple parlant pour la durabilité
Choisir un ordinateur reconditionné plutôt qu’un neuf permet d’éviter des dizaines de kilos de CO2 et l’extraction de grandes quantités de matières premières, tout en réduisant les déchets électroniques. Dans les écoles, le reconditionnement permet en plus d’équiper davantage d’élèves à moindre coût et, lorsqu’il est fait avec les élèves (comme dans la démarche NIRD), de transformer cet acte écologique en projet pédagogique concret autour de la sobriété numérique et de l’économie circulaire.</p>
