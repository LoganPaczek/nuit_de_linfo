                <p>
                    Les établissements scolaires arrivent à la fin du support de Windows 10, ce qui les pousse soit à racheter des licences et parfois du matériel, soit à chercher des alternatives, alors même que les ordinateurs fonctionnent encore.<br>
                    Cette situation crée un triple problème : gaspillage écologique (déchets électroniques), pression financière sur les budgets publics, et dépendance forte aux grandes entreprises du numérique.<br>
                    Face à cela, des enseignants, techniciens, élèves et collectivités cherchent une autre voie : logiciels libres, Linux, reconditionnement, communs numériques, pour construire un numérique scolaire plus autonome, inclusif et durable.<br> 
                    C’est dans ce contexte de rupture avec le modèle imposé par les Big Tech que naît la démarche NIRD, comme réponse de terrain et mouvement de « résistance numérique » dans l’école.<br>
