            <p>Linux NIRD : une distribution faite par et pour l’école <br>
Linux NIRD est une distribution GNU/Linux pensée pour les collèges et lycées, conçue et maintenue par des enseignants engagés dans la démarche NIRD. Elle offre un environnement léger, moderne et complet, adapté aux usages pédagogiques du secondaire, y compris pour la spécialité NSI.<br>
Linux NIRD est optimisée pour fonctionner sur des ordinateurs récents comme sur du matériel ancien reconditionné : démarrage rapide, interface simple à prendre en main, et système stable pour un usage quotidien en classe ou en salle des professeurs. Elle embarque une sélection de logiciels libres couvrant les besoins courants (bureautique, web, sciences, multimédia, programmation, robotique, NSI), afin que les enseignants puissent travailler immédiatement sans longues phases d’installation.<br
La distribution peut être testée depuis une clé USB sans toucher au système déjà présent, puis installée de manière pérenne sur les postes retenus, notamment dans le cadre d’ateliers de reconditionnement menés avec les élèves. Autour de Linux NIRD, une communauté d’enseignants propose entraide, tutoriels et retours d’expérience, ce qui facilite la prise en main et sécurise les équipes qui débutent sous Linux.</p>
